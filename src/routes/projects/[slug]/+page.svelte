							<h3 class="text-brand-primary not-prose mb-3 text-xl font-semibold">The Challenge</h3>
							<p>
								SecureFinance Inc. needed a reliable and performant platform to aggregate financial
								data from disparate sources for their B2B clients. Key requirements included high
								security, real-time data processing capabilities, multi-tenancy support, and an
								intuitive interface for data exploration and reporting.
							</p>
						</div>

						<div>
							<h3 class="text-brand-primary not-prose mb-3 text-xl font-semibold">Our Solution</h3>
							<p>
								Qbin Studio designed and developed a full-stack solution leveraging a
								microservices-inspired architecture. The frontend, built with Next.js and
								TypeScript, provides a responsive and interactive user dashboard. The backend
								consists of Node.js and Python services responsible for data ingestion, processing,
								and API delivery, communicating via a message queue.
							</p>
							<p>
								We utilized PostgreSQL for structured relational data and Redis for caching and
								session management. The entire platform is deployed on AWS using Docker containers,
								Lambda functions for specific tasks, and S3 for static asset storage, ensuring
								scalability and reliability.
							</p>
						</div>

